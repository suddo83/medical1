a = int(input("첫번째 숫자를 입력하시오 >> "))
b = int(input("두번째 숫자를 입력하시오 >> "))
print('{}+{}={}'.format(a,b,a+b))
print('{}-{}={}'.format(a,b,a-b))
print('{}*{}={}'.format(a,b,a*b))
print('{}/{}={:.3f}'.format(a,b,a/b))

