#최대값, 최소값 구하기

listdata = [9.96, 1.27, 5.07, 6.45, 8.38, 9.29, 4.93, 7.73, 3.71, 0.93]

maxval = max(listdata)
minval = min(listdata)
print('최대값 : ', maxval)
print('최소값 : ', minval)

txt = 'Alotofthingsoccureachday'
maxval = max(txt)
minval = min(txt)
print('최대값 : ', maxval)
print('최소값 : ', minval)
